//CONVERTIR GRADOS A CELCIUS A FAHRENHEIT
// 1. Crea una FUNCION EXPRESADA llamada convertirCelciosAFahrenheit que reciba una temperatura en celciu y evuelva el otro

function convertirCelciosAFahrenheit (celcius){
    return celcius * 9/5 + 32
} 
console.log("La temperatura en Fahrenheit es de ", convertirCelciosAFahrenheit(20));