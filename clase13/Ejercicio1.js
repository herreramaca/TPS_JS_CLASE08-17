// ÁREA DE UN TRIANGULO (FUNCIÓN DECLARADA)
// 1. Creo un funcion llamada calcularAreaTriangulo que reciba dos parametros (base, altura)
function calcularAreaTriangulo (base, altura) {
    return base * altura / 2
}

// 2. La función debe devolver el área de un triángulo
console.log("El área del triangulo es", calcularAreaTriangulo (4, 5));